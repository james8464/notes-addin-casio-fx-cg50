const int helpen_size=sizeof(helpen)/sizeof(charptrint);

int dichotomic_search(const charptrint * tab,unsigned tab_size,const char * s){
  int beg=0,end=tab_size,cur,test;
  // string index is always >= begin and < end
  for (;;){
    cur=(beg+end)/2;
    test=strcmp(s,tab[cur].s);
    if (!test)
      return cur;
    if (cur==beg)
      return -1;
    if (test>0)
      beg=cur;
    else
      end=cur;
  }
  return -1;
}

int longhelp_pos(const char * s){
  int pos=dichotomic_search(helpen,helpen_size,s);
  if (pos==-1)
    return pos;
  return helpen[pos].i;
}

ustl::string printint(int i){
  if (!i)
    return ustl::string("0");
  if (i<0)
    return ustl::string("-")+printint(-i);      
  int length = (int) std::floor(std::log10((double) i));
  char s[32];
  s[length+1]=0;
  for (;length>-1;--length,i/=10)
    s[length]=i%10+'0';
  return s;
}
  
ustl::string longhelp(const char * s){
  ustl::string cmd(s);
  for (int i=0;i<cmd.size();++i){
    if (cmd[i]=='(' || cmd[i]==' ')
      cmd=cmd.substr(0,i);
  }
  int pos=longhelp_pos(cmd.c_str());
  if (pos==-1)
    return "index.html";
  ustl::string pos_s=printint(pos);
  if (pos_s.size()==1)
    pos_s="00"+pos_s;
  else if (pos_s.size()==2)
    pos_s="0"+pos_s;    
  return ustl::string("cascmd_en")+pos_s+".html";
}
